    private void botonAceptarInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarInicioActionPerformed
        
        String nci = numeroCuentaInicio.getText();
        String cci = claveCuentaInicio.getText();
        
        if (nci.length()>0 && cci.length()>0){
            
            MenuOpcionesCajero2 opciones = new MenuOpcionesCajero2(this, true);
            opciones.setNumeroCuenta(nci);
            opciones.setClaveCuenta(cci);
            opciones.setParent(this);
            connectBD.consultarCuenta(nci, cci, opciones);
        }
        else{
            JOptionPane.showMessageDialog(null, "Campos vacios");        
        }
        
        
        
        
    }

